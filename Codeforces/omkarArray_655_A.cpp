#include<iostream>
#include <bits/stdc++.h>
using namespace std;

int main()
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    
    int t;
    cin>>t;

    for(int i=0;i<t;i++)
    {
        int n;
        cin>>n;

        for(int j=0;j<n;j++)
            cout<<1<<" ";
        cout<<"\n";
    }

    return 0;
}
